/**
 * Mux (mux.com) integration reference for NexusMedia.
 * Enables cloud encoding, HLS/DASH streaming, subtitle uploads (.vtt), and analytics.
 *
 * Usage:
 * 1. Upload local video to Mux: POST /assets -> playback_id
 * 2. Play via HLS: https://stream.mux.com/{playback_id}.m3u8
 * 3. Upload subtitle (.vtt from WhisperSubtitleGenerator): POST /assets/{asset_id}/tracks
 * 4. Analytics: GET /data/v1/metrics for view counts/playback duration
 */
object MuxVideoService {

    // Mux API base
    const val MUX_BASE_URL = "https://api.mux.com"
    const val MUX_STREAM_URL_TEMPLATE = "https://stream.mux.com/{playback_id}.m3u8"

    // Mux supports subtitle uploads (.vtt) per asset for multi-language playback
    fun buildSubtitleTrackUrl(assetId: String, languageCode: String = "en"): String {
        return "$MUX_BASE_URL/assets/$assetId/tracks?language_code=$languageCode&type=text"
    }

    // Mux analytics reference
    fun analyticsEndpoint(assetId: String): String {
        return "$MUX_BASE_URL/data/v1/assets/$assetId/metrics"
    }

    // Playback URL generator for NexusMedia player
    fun playbackUrl(playbackId: String): String {
        return MUX_STREAM_URL_TEMPLATE.replace("{playback_id}", playbackId)
